#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	res;
	int				neg;

	neg = 1;
	res = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r' ||
			*str == '\v' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			neg = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - 48);
		str++;
	}
	return ((int)(res * neg));
}
